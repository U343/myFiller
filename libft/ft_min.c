#include "libft.h"

int		ft_min(int *t, size_t size)
{
	size_t	i;
	int		res;

	res = 0;
	if (t)
	{
		i = 0;
		while (i < size)
		{
			if (res > t[i])
				res = t[i];
			i++;
		}
	}
	return (res);
}
